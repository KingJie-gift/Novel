package entity;

public class Phone {
	private int smsid;
	private String phone;
	private int code;
	
	public int getSmsid() {
		return smsid;
	}
	public void setSmsid(int smsid) {
		this.smsid = smsid;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	
	
}
